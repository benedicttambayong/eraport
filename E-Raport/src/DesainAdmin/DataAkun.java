    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int indeks;
        indeks=tabakun.getSelectedRow();
        String nip=tabakun.getModel().getValueAt(indeks,0).toString();
        
        database e = new eraport.database();
                String s = "select * from account where nip='"+nip+"'";
           try
           {
                e.rs = e.getdata(s);
                if(e.rs.next()){
                    JOptionPane.showMessageDialog(null, "Data Found");
                     TambahAkun ta = new TambahAkun();
                    ta.setVisible(true);
        
                    ta.create_nip.setText(e.rs.getString("nip"));
                    ta.create_nip.disable();
                    ta.create_email.setText(e.rs.getString("email"));
                    ta.create_user.setText(e.rs.getString("username"));
                    ta.create_pass.setText(e.rs.getString("password"));
                    int jenis=0;
                    if("Guru Mapel".equals(e.rs.getString("jenis")))
                    {
                        jenis=0;
                    }
                    else if("Wali Murid".equals(e.rs.getString("jenis")))
                    {
                        jenis=1;
                    }
                    ta.create_jenis.setSelectedIndex(jenis);
                    
                    
            }
            else{
                JOptionPane.showMessageDialog(null, "Data not found");
            }
           }    
            
            catch (SQLException f) {
            javax.swing.JOptionPane.showMessageDialog(null,"Error"+f.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed


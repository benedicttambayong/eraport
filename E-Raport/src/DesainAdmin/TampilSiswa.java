     public void loadsiswa()
    {
         database d = new eraport.database();
         DefaultTableModel model = (DefaultTableModel) tabsiswa.getModel();
         
         database da = new eraport.database();
        
            
            
        try {
            String s = "select nis,nama from siswa ";
            da.getdata(s);
            int i =0,j=0;
            while(da.rs.next()){
                j++;
            }
            model.setRowCount(j);
            d.getdata(s);
            while(d.rs.next()){
                tabsiswa.setValueAt(d.rs.getString("nis"), i, 0);
                tabsiswa.setValueAt(d.rs.getString("nama"), i, 1);
               
                i++;
            }
        } catch (Exception e) {
        }
        tabsiswa.setAutoCreateRowSorter(true);
    }

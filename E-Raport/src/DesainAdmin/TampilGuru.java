    public void loadguru()
    {
         DefaultTableModel model = (DefaultTableModel) tabguru.getModel();
         database d = new eraport.database();
         database da = new eraport.database();
        try {
            String s = "select nip,nama,mapel from guru ";
            
            
            da.getdata(s);
            int i =0,j=0;
            while(da.rs.next()){
                j++;
            }
            model.setRowCount(j);
            d.getdata(s);
            while(d.rs.next()){
                tabguru.setValueAt(d.rs.getString("nip"), i, 0);
                tabguru.setValueAt(d.rs.getString("nama"), i, 1);
                tabguru.setValueAt(d.rs.getString("mapel"), i, 2);
                
                i++;
            }
        } catch (Exception e) {
        }
        tabguru.setAutoCreateRowSorter(true);
    }
    
    
             
             


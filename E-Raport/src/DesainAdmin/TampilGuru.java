    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int indeks;
        indeks=tabguru.getSelectedRow();
        String nip=tabguru.getModel().getValueAt(indeks,0).toString();
        
        database e = new eraport.database();
                String s = "select * from guru where nip='"+nip+"'";
           try
           {
                e.rs = e.getdata(s);
                if(e.rs.next()){
                    JOptionPane.showMessageDialog(null, "Data Found");
                     TambahGuru tg = new TambahGuru();
                    tg.setVisible(true);
        
                    tg.create_nip.setText(e.rs.getString("nip"));
                    tg.create_nip.disable();
                    tg.create_nama.setText(e.rs.getString("nama"));
                    tg.create_alamat.setText(e.rs.getString("alamat"));
                    tg.create_telp.setText(e.rs.getString("telp"));
                    tg.create_email.setText(e.rs.getString("email"));
                    tg.create_tempatlahir.setText(e.rs.getString("tempat_lahir"));
                    tg.create_tanggal.setSelectedIndex(0);
                    tg.create_bulan.setSelectedIndex(0);
                    tg.create_tahun.setSelectedIndex(0);
                    int agama=0;
                    if("Islam".equals(e.rs.getString("agama")))
                    {
                        agama=0;
                    }
                    else if("Kristen".equals(e.rs.getString("agama")))
                    {
                        agama=1;
                    }
                    else if("Katholik".equals(e.rs.getString("agama")))
                    {
                        agama=2;
                    }
                    else if("Hindu".equals(e.rs.getString("agama")))
                    {
                        agama=3;
                    }
                    else if("Budha".equals(e.rs.getString("agama")))
                    {
                        agama=4;
                    }
                    tg.create_agama.setSelectedIndex(agama);
                    
                    int status=0;
                    if("Menikah".equals(e.rs.getString("status")))
                    {
                        status=0;
                    }
                    else if("Belum Menikah".equals(e.rs.getString("status")))
                    {
                        status=1;
                    }
                    tg.create_status.setSelectedIndex(status);
                    
                    int jk=0;
                    if("Laki-laki".equals(e.rs.getString("jk")))
                    {
                        jk=0;
                    }
                    else if("Perempuan".equals(e.rs.getString("jk")))
                    {
                        jk=1;
                    }
                    tg.create_jeniskelamin.setSelectedIndex(jk);
                    
                    int mapel=0;
                    if("Matematika".equals(e.rs.getString("mapel")))
                    {
                        mapel=0;
                    }
                    else if("Bahasa Indonesia".equals(e.rs.getString("mapel")))
                    {
                        mapel=1;
                    }
                    else if("Fisika".equals(e.rs.getString("mapel")))
                    {
                        mapel=2;
                    }
                    else if("Biologi".equals(e.rs.getString("mapel")))
                    {
                        mapel=3;
                    }
                    else if("Kimia".equals(e.rs.getString("mapel")))
                    {
                        mapel=4;
                    }
                    else if("Bahasa Inggris".equals(e.rs.getString("mapel")))
                    {
                        mapel=5;
                    }
                    else if("Geografi".equals(e.rs.getString("mapel")))
                    {
                        mapel=6;
                    }
                    else if("Sosiologi".equals(e.rs.getString("mapel")))
                    {
                        mapel=7;
                    }
                    else if("Akuntansi".equals(e.rs.getString("mapel")))
                    {
                        mapel=8;
                    }
                    else if("Olahraga".equals(e.rs.getString("mapel")))
                    {
                        mapel=9;
                    }
                    tg.create_mapel.setSelectedIndex(1);
            }
            else{
                JOptionPane.showMessageDialog(null, "Data not found");
            }
           }    
            
            catch (SQLException f) {
            javax.swing.JOptionPane.showMessageDialog(null,"Error"+f.getMessage());
        }
       
    }//GEN-LAST:event_jButton4ActionPerformed


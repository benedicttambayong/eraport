    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       int indeks;
        indeks=tabsiswa.getSelectedRow();
        String nis=tabsiswa.getModel().getValueAt(indeks,0).toString();
        
        database e = new eraport.database();
                String s = "select * from siswa where nis='"+nis+"'";
           try
           {
                e.rs = e.getdata(s);
                if(e.rs.next()){
                    JOptionPane.showMessageDialog(null, "Data Found");
                     TambahSiswa ts = new TambahSiswa();
                    ts.setVisible(true);
        
                    ts.create_nis.setText(e.rs.getString("nis"));
                    ts.create_nis.disable();
                    ts.create_nama.setText(e.rs.getString("nama"));
                    ts.create_alamat.setText(e.rs.getString("alamat"));
                    ts.create_telp.setText(e.rs.getString("telp"));
                    ts.create_email.setText(e.rs.getString("email"));
                    ts.create_tempatlahir.setText(e.rs.getString("tempat_lahir"));
                    ts.create_tanggal.setSelectedIndex(0);
                    ts.create_bulan.setSelectedIndex(0);
                    ts.create_tahun.setSelectedIndex(0);
                    ts.create_kelas.setText(e.rs.getString("kelas"));
                    ts.create_ijasah.setText(e.rs.getString("ijasah"));
                    ts.create_tahunajaran.setText(e.rs.getString("tahunajaran"));
                    ts.create_skhu.setText(e.rs.getString("skhu"));
                    int agama=0;
                    if("Islam".equals(e.rs.getString("agama")))
                    {
                        agama=0;
                    }
                    else if("Kristen".equals(e.rs.getString("agama")))
                    {
                        agama=1;
                    }
                    else if("Katholik".equals(e.rs.getString("agama")))
                    {
                        agama=2;
                    }
                    else if("Hindu".equals(e.rs.getString("agama")))
                    {
                        agama=3;
                    }
                    else if("Budha".equals(e.rs.getString("agama")))
                    {
                        agama=4;
                    }
                    ts.create_agama.setSelectedIndex(agama);
                    
                    
                    int jk=0;
                    if("Laki-laki".equals(e.rs.getString("jk")))
                    {
                        jk=0;
                    }
                    else if("Perempuan".equals(e.rs.getString("jk")))
                    {
                        jk=1;
                    }
                    ts.create_jeniskelamin.setSelectedIndex(jk);
                    
                    
            }
            else{
                JOptionPane.showMessageDialog(null, "Data not found");
            }
           }    
            
            catch (SQLException f) {
            javax.swing.JOptionPane.showMessageDialog(null,"Error"+f.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed


    public void loadakun()
    {
         DefaultTableModel model = (DefaultTableModel) tabakun.getModel();
         database d = new eraport.database();
         database da = new eraport.database();
        try {
            String s = "select nip,email,jenis from account ";
            
            
            da.getdata(s);
            int i =0,j=0;
            while(da.rs.next()){
                j++;
            }
            model.setRowCount(j);
            d.getdata(s);
            while(d.rs.next()){
                tabakun.setValueAt(d.rs.getString("nip"), i, 0);
                tabakun.setValueAt(d.rs.getString("email"), i, 1);
                tabakun.setValueAt(d.rs.getString("jenis"), i, 2);
                i++;
            }
        } catch (Exception e) {
        }
        tabakun.setAutoCreateRowSorter(true);
    }
